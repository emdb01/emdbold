				<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12" style="padding: 20px 0px 50px;">
				<h1 style="color:"><center>Why Employee Master Database</center></h1></br>
				
 
			<p>  
Employeemasterdatabase.com is a repository of employee profiles. It effectively bridges the gap between the recruiters and job seekers by providing them a common platform. Through EMDB system, job seekers can indicate their availability/non-availability for job openings on need basis. This significantly increases the chance of recruiter connecting with the suitable candidate and thus makes the whole recruitment process simpler.</p></br>
  <p>  
In addition to confirming the availability status of the job seekers, EMDB helps the recruiters to reuse their existing resume database for every new job requirement. As registering with EMDB avoids the need to purchase resume database each time there is a new job requirement, it proves to be a cost-efficient process.</p></br>
 
<p>  
EMDB helps job seekers to improve their chances of getting calls from the recruiter. Job seekers need to update their availability status as and when it changes. When job seekers change their status to "Available", it shows up in recruiter search and is most likely to be preferred by the recruiter. On the other hand the when job seekers are not looking for a change, they can avoid calls from recruiters by updating the status to "Not Available" in EMDB website. This process saves the valuable time of both recruiters and job seekers.</p>
